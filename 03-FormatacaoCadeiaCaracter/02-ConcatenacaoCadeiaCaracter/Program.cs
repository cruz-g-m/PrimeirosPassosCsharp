/*Para concatenar duas cadeias de caracteres,
use o operador de concatenação de cadeia de caracteres, que é o símbolo de adição +.*/
Console.WriteLine("");
string firstName = "Bob";
string message = "Hello " + firstName;
Console.WriteLine(message);

